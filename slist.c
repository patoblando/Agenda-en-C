//Esta funciÃ³n destruye todos los nodos de la lista haciendo que 
//liberemos la memoria del nodo usando free y, pasando al siguiente
//hasta llegar a NULL

void slist_destruir(SList lista) {
  SNodo *nodoAEliminar;
  while (lista != NULL) {
    nodoAEliminar = lista;
    lista = lista->sig;
    free(nodoAEliminar);
  }
}

